num1=float(input("ingrese el numero uno: "))
num2=float(input("ingrese el numero dos: "))
num3=float(input("ingrese el numero tres: "))

mayor=max(num1,num2,num3)
menor= min(num1,num2,num3)
medio =(num1 + num2 + num3)- mayor - menor
print("el valor mayor es",mayor)
print("el valor mediano es",medio)
print("el valor menor es",menor)

